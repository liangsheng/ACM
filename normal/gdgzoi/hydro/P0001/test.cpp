#include <bits/stdc++.h>  // 头文件, 类似 python 的 import
using namespace std;  // 暂时不用理解

int main() {
    // 代码逻辑从这里开始
    
    // char 转 int
    cout << (int) 'a' << endl;
    cout << (int) 'c' << endl;
    cout << (int) 'z' << endl;

    cout << (int) 'A' << endl;
    cout << (int) 'C' << endl;
    cout << (int) 'Z' << endl;

    cout << (int) '0' << endl;
    cout << (int) '3' << endl;
    cout << (int) '9' << endl;

    // int 转 char
    cout << (char) 97 << endl;
    cout << (char) 99 << endl;
    cout << (char) 122 << endl;

    cout << (char) 65 << endl;
    cout << (char) 67 << endl;
    cout << (char) 90 << endl;

    cout << (char) 48 << endl;
    cout << (char) 51 << endl;
    cout << (char) 57 << endl;

    // 代码逻辑在这里结束
	return 0;
}